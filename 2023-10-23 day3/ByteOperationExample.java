package chapter02;

public class ByteOperationExample {
	public static void main(String[] args) {
		byte result1=10+20;
	// 연산식 > 변수	
		System.out.println(result1);
	
		
		byte x=10;
		byte y=20;
		// x+y = 오류 , 연산 > int로 변환 
		byte result2 =(byte)(x+y);
		System.out.println(result2);
	
	
	
	
	
	
	}
}
