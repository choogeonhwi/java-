package Q6_3;

import java.util.ArrayList;

public class Singer {
	private String n;
	private int m;
	private ArrayList<String> s;
	
	
	
	public Singer(String n) {
	
		this.n = n;
	}
	
	
	public String getN() {
		return n;
	}
	public void setN(String n) {
		this.n = n;
	}
	public int getM() {
		return m;
	}
	public void setM(int m) {
		this.m = m;
	}
	public ArrayList<String> getS() {
		return s;
	}
	public void setS(ArrayList<String> s) {
		this.s = s;
	}


	

	
}
